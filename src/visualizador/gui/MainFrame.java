    private void analizaJarFile() throws HeadlessException {
        statusLabel.setText("Analizando ficheros JAR");
        try {
            DefaultListModel model = new DefaultListModel();
            ZipInputStream zip = new ZipInputStream(new FileInputStream(jarNombre.getText()));
            for (ZipEntry entry = zip.getNextEntry(); entry != null; entry = zip.getNextEntry()) {
                model.addElement(entry.getName());
            }
            jarEntradas.setModel(model);
            
            statusLabel.setText("- Ficheros detectados 'L', : " + model.size());
        } catch (IOException ioe) {
            statusLabel.setText("- Archivos encontrados: 0");
            JOptionPane.showMessageDialog(this, ioe.getLocalizedMessage(), "'L'.  Ups!- Errorcito a la vista", JOptionPane.ERROR_MESSAGE);
        }
    }

